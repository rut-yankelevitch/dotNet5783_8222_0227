namespace DO;
public struct User
{
    /// <summary>
    /// Id of User
    /// </summary>
    public int ID { get; set; }

    /// <summary>
    /// Name of User
    /// </summary>
    public string? Name { get; set; }

    /// <summary>
    /// password of User
    /// </summary>
    public string? Password { get; set; }

    /// <summary>
    /// Email of User
    /// </summary>
    public string? Email { get; set; }

    /// <summary>
    /// Address of user
    /// </summary>
    public string? Address { get; set; }
}
